phrase = "Vedant is Awesome"

#print upper case and check for uppercase
print(phrase.upper().isupper())

#print the length of the phrase
print(len(phrase))

#find the index of the character
print(phrase[3])

#index function (location of the variable)
print(phrase.index("is Awesome"))

#replace the phrase
print(phrase.replace("Awesome","cool"))
